package seedu.addressbook.data.address;

/**
 * Represents a Block in the address.
 */
public class Block {

    private String block;

    public Block(String block) {
        this.block = block;
    }

    public String getBlock() {
        return block;
    }

    public void setBlock(String block) {
        this.block = block;
    }
}
